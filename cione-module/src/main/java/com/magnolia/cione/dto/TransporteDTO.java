package com.magnolia.cione.dto;

public class TransporteDTO {
	private String codigo;
	private String nombre;
	private String direccionEnvio;
	private String agencia;
	private String agenciaBolsa;
	private String agenciaPaqueteria;
	private String entregaSabado;
	public String getCodigo() {
		return codigo;
	}
	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getDireccionEnvio() {
		return direccionEnvio;
	}
	public void setDireccionEnvio(String direccionEnvio) {
		this.direccionEnvio = direccionEnvio;
	}
	public String getAgencia() {
		return agencia;
	}
	public void setAgencia(String agencia) {
		this.agencia = agencia;
	}
	public String getAgenciaBolsa() {
		return agenciaBolsa;
	}
	public void setAgenciaBolsa(String agenciaBolsa) {
		this.agenciaBolsa = agenciaBolsa;
	}
	public String getAgenciaPaqueteria() {
		return agenciaPaqueteria;
	}
	public void setAgenciaPaqueteria(String agenciaPaqueteria) {
		this.agenciaPaqueteria = agenciaPaqueteria;
	}
	public String getEntregaSabado() {
		return entregaSabado;
	}
	public void setEntregaSabado(String entregaSabado) {
		this.entregaSabado = entregaSabado;
	}
	
	
	
}
