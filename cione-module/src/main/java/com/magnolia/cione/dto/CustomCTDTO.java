package com.magnolia.cione.dto;

public class CustomCTDTO {
	
	private TypeCTDTO type;
	private FieldsCTDTO fields;
	
	public TypeCTDTO getType() {
		return type;
	}
	
	public void setType(TypeCTDTO type) {
		this.type = type;
	}
	
	public FieldsCTDTO getFields() {
		return fields;
	}
	
	public void setFields(FieldsCTDTO fields) {
		this.fields = fields;
	}

}
