package com.magnolia.cione.dto;

public class CioneLabDTO {
	private String idSocio;
	private String grupoPrecio;
	private String lang;
	private String visibilidad;
	private String idCarrito;
	
	private String idCarritoOculto;
	private String pack;
	private String skuMontura;
	private String skuPackMaster;
	
	public String getIdSocio() {
		return idSocio;
	}
	public void setIdSocio(String idSocio) {
		this.idSocio = idSocio;
	}
	public String getGrupoPrecio() {
		return grupoPrecio;
	}
	public void setGrupoPrecio(String grupoPrecio) {
		this.grupoPrecio = grupoPrecio;
	}
	public String getLang() {
		return lang;
	}
	public void setLang(String lang) {
		this.lang = lang;
	}
	public String getVisibilidad() {
		return visibilidad;
	}
	public void setVisibilidad(String visibilidad) {
		this.visibilidad = visibilidad;
	}
	public String getIdCarrito() {
		return idCarrito;
	}
	public void setIdCarrito(String idCarrito) {
		this.idCarrito = idCarrito;
	}
	public String getIdCarritoOculto() {
		return idCarritoOculto;
	}
	public void setIdCarritoOculto(String idCarritoOculto) {
		this.idCarritoOculto = idCarritoOculto;
	}
	public String getPack() {
		return pack;
	}
	public void setPack(String pack) {
		this.pack = pack;
	}
	public String getSkuMontura() {
		return skuMontura;
	}
	public void setSkuMontura(String skuMontura) {
		this.skuMontura = skuMontura;
	}
	public String getSkuPackMaster() {
		return skuPackMaster;
	}
	public void setSkuPackMaster(String skuPackMaster) {
		this.skuPackMaster = skuPackMaster;
	}
}
