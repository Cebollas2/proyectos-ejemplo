package com.magnolia.cione.dto;

public class LookticDTO {
	
	private String codigo;
	private String nombre;
	private String direccionEnvio;
	private String agencia;
	private String emailDashboard;
	private String nivelAdhesion;
	private String fechaAdhesion;
	public String getCodigo() {
		return codigo;
	}
	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getDireccionEnvio() {
		return direccionEnvio;
	}
	public void setDireccionEnvio(String direccionEnvio) {
		this.direccionEnvio = direccionEnvio;
	}
	public String getAgencia() {
		return agencia;
	}
	public void setAgencia(String agencia) {
		this.agencia = agencia;
	}
	public String getEmailDashboard() {
		return emailDashboard;
	}
	public void setEmailDashboard(String emailDashboard) {
		this.emailDashboard = emailDashboard;
	}
	public String getNivelAdhesion() {
		return nivelAdhesion;
	}
	public void setNivelAdhesion(String nivelAdhesion) {
		this.nivelAdhesion = nivelAdhesion;
	}
	public String getFechaAdhesion() {
		return fechaAdhesion;
	}
	public void setFechaAdhesion(String fechaAdhesion) {
		this.fechaAdhesion = fechaAdhesion;
	}
	
	
}
