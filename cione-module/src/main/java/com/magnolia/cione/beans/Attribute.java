package com.magnolia.cione.beans;

public class Attribute{
	
    public String name;
    public Object value;
    
    public Attribute() {}
    
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Object getValue() {
		return value;
	}
	public void setValue(Object value) {
		this.value = value;
	}
    
    
}